package Article;

public class article {
	int id;
	String name;
	String img;
	String dec;
	int xid;
	public int getId(){
		return this.id;
	}
	public void setId(int id){
		this.id = id;
	}
	public String getName(){
		return this.name;
	}
	public void setName(String name){
		this.name = name;
	}
	public String getImg(){
		return this.img;
	}
	public void setImg(String img){
		this.img = img;
	}
	public String getDec(){
		return this.dec;
	}
	public void setDec(String dec){
		this.dec = dec;
	}
	public int getXid(){
		return this.xid;
	}
	public void setXid(int xid){
		this.xid = xid;
	}
}
